void remove_pcap(void)
{
	int Status;
	XDcfg_Config *ConfigPtr0;

	ConfigPtr0 = XDcfg_LookupConfig(DCFG_DEVICE_ID);

	Status = XDcfg_CfgInitialize(&DcfgInstance, ConfigPtr0,
					ConfigPtr0->BaseAddr);
	if (Status != XST_SUCCESS) {
		print("DevConfig - Fail !\n\r");
	}

	/*
	 * Run the Self Test.
	 */
	Status = XDcfg_SelfTest(&DcfgInstance);
	if (Status != XST_SUCCESS) {
		print("SelftTest - Fail !\n\r");
	}

	XDcfg_SelectIcapInterface(&DcfgInstance);
}

//DMA init function
int dma_init(XAxiDma_Config* cfg_ptr){
		int status = 0;

		xil_printf("Initializing DMA...\n\r");

		// Look up hardware configuration for device
		cfg_ptr = XAxiDma_LookupConfig(XPAR_AXIDMA_0_DEVICE_ID);
		if (!cfg_ptr)
		{
			xil_printf("No config found for %d\r\n", XPAR_AXIDMA_0_DEVICE_ID);
			return XST_FAILURE;
		}

		// Initialize driver
		status = XAxiDma_CfgInitialize(&axi_dma, cfg_ptr);
		if (status != XST_SUCCESS)
		{
			xil_printf("Initialization failed %d\r\n", status);
			return XST_FAILURE;
		}

		// Test for Scatter Gather
		if (XAxiDma_HasSg(&axi_dma))
		{
			xil_printf("Device configured as SG mode \r\n");
			return XST_FAILURE;
		}

		// Disable interrupts for both channels
		XAxiDma_IntrDisable(&axi_dma, XAXIDMA_IRQ_ALL_MASK, XAXIDMA_DEVICE_TO_DMA);
		XAxiDma_IntrDisable(&axi_dma, XAXIDMA_IRQ_ALL_MASK, XAXIDMA_DMA_TO_DEVICE);

		xil_printf("DMA initialization complete!\n\r");

		return XST_SUCCESS;
}
//Init end

int main(void) {

	XAxiDma_Config *cfg_ptr;
	int status;
	status = dma_init(cfg_ptr);
	if (status != XST_SUCCESS)
	{
		xil_printf("ERROR! DMA initialization failed!\n\r");
		return XST_FAILURE;
	}

	status = XGpio_Initialize(&axi_gpio, XPAR_GPIO_SW_DEVICE_ID);
	if (status != XST_SUCCESS)
	{
		xil_printf("ERROR! GPIO initialization failed!\n\r");
		return XST_FAILURE;
	}

	XGpio_DiscreteWrite(&axi_gpio, 1, 0x0); // Put data source back in reset state for first run

	remove_pcap();
	xil_printf("Starting FIR test...\n\r\n\r");

	test_fir(&axi_gpio, &axi_dma);

	xil_printf("End EXOR test.\n\r\n\r");
	return 0;
}
