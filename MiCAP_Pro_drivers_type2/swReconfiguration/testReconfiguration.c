inline u8 getBit(int word,int i) {
    return (word & (1 << i)) != 0;
}

void setParameterbits(int word, int length, u8 *parameter) {
    int i;
    for(i=0;i<length;i++)
        *(parameter++) = getBit(word, i);
}


void copyLutConfig(LUT_config_type *lut_config, const lutlocation *location, u8 newtruthtable[LUT_CONFIG_WIDTH]) {
	lut_config->slice_col = location->lutCol;
	lut_config->slice_row = location->lutRow;
	lut_config->Resource = XHI_CLB_LUT_replacement.CONTENTS[location->sliceType][location->lutType];
	lut_config->NumBits = LUT_CONFIG_WIDTH;
	lut_config->Value = newtruthtable;
}

u32 presort(const lutlocation *location_array,
        u8 *newtruthtables,
		LUT_config_type *lut_configs,
		u32 *group_sizes,
		u32 number_of_tluts) {

	u8 is_sorted[number_of_tluts];
	memset(is_sorted, 0x0, sizeof(u8)*number_of_tluts);

	u32 num_sorted = 0;
	u8 group_num = 0;
	u32 i;
	while(num_sorted != number_of_tluts) {
		for(i = 0; i<number_of_tluts; i++) {
			if(!is_sorted[i])
				break;
		}
		assert(i != number_of_tluts);
		group_num++;
		num_sorted++;
		group_sizes[group_num-1] = 1;
		copyLutConfig(&lut_configs[num_sorted-1], &location_array[i], &newtruthtables[i]);
		is_sorted[i] = 1;
		for(; i<number_of_tluts; i++) {
			if(is_sorted[i])
				continue;
			if(MiCAP_Custom_IsSameFrame(lut_configs[num_sorted-1].slice_row, lut_configs[num_sorted-1].slice_col,
					location_array[i].lutRow, location_array[i].lutCol)) {
				num_sorted++;
				group_sizes[group_num-1]++;
				copyLutConfig(&lut_configs[num_sorted-1], &location_array[i], &newtruthtables[i]);
				is_sorted[i] = 1;
			}
		}
	}
	group_sizes[group_num] = 0;
	return group_num;
}

//preload configuration frames
void preload_frames(XGpio* gpio_InstancePtr, XAxiDma* dma_InstancePtr, LUT_config_type *lut_configs,	u32 *group_sizes, u32 *config_buffer) {
	//load the configuration frames of each group one by one
	u32 i;
	LUT_config_type *lut_config_p = lut_configs;
	u32 *config_buffer_p = config_buffer;
	for(i=0;group_sizes[i]!=0;i++) {
		XStatus Status;
		Status = MiCAP_Custom_ReadClbFrames(gpio_InstancePtr, dma_InstancePtr ,lut_config_p, config_buffer_p);
		assert(Status == XST_SUCCESS);
		lut_config_p += group_sizes[i];
		//xil_printf("preload frames group %d\n\r", i);
		config_buffer_p += MiCAP_Custom_GetClbFramesSize();
	}
}

//reconfigure one instance with preloaded configuration frames
void reconfigure_group_with_frames(XGpio* gpio_InstancePtr, XAxiDma* dma_InstancePtr, LUT_config_type *lut_configs,	u32 *group_sizes, u32 *config_buffer) {
	//reconfigure each group of TLUTs one by one
	u32 i;
	LUT_config_type *lut_config_p = lut_configs;
	u32 *config_buffer_p = config_buffer;
	for(i=0;group_sizes[i]!=0;i++) {
		XStatus Status;
		Status = MiCAP_Custom_SetMultiClbBitsWithFrames(gpio_InstancePtr, dma_InstancePtr, lut_config_p, group_sizes[i], config_buffer_p);
		assert(Status == XST_SUCCESS);
		lut_config_p += group_sizes[i];
		//xil_printf("reconfig group %d\n\r", i);
		config_buffer_p += MiCAP_Custom_GetClbFramesSize();
	}
}


